#include "main.h"
/**
 * main - Starting point function
 * Description: Invokes a function print_alphabet
 * Return: 0 (Success)
 * print_alphabet -dbdndnnddjjdd
 * Description: hsjsjdjdjdjj
 */
void print_alphabet(void)
{
	char alpha = 'a';

	while (alpha <= 'z')
	{
		_putchar(alpha);
		alpha++;
	}
	_putchar('\n');
}
print_alphabet();
return (0);
